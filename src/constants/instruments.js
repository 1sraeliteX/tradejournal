export function findInstrument(symbol) {
  if (!symbol) return null;
  const upper = symbol.toUpperCase();
  return ALL_INSTRUMENTS.find((i) => i.symbol === upper) || null;
}

export function instrumentsByCategory(category) {
  if (!category) return ALL_INSTRUMENTS;
  return ALL_INSTRUMENTS.filter((i) => i.category === category);
}
